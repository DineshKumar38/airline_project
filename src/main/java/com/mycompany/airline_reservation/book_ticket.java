    public void autoId() throws SQLException {
        try {
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline_resevation", "root", "dineshdk");
            pre = con.prepareStatement("Select MAX(ticket_id)from ticket");
            ResultSet rs = pre.executeQuery();
            rs.next();

            if (rs.getString("MAX(ticket_id)") == null) {
                id.setText("TK001");
            } else {
                long ida = Long.parseLong(rs.getString("MAX(ticket_id)").substring(2, rs.getString("MAX(ticket_id)").length()));
                ida++;
                id.setText("TK" + String.format("%03d", ida));
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(add_customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

