    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            String arr = arrival.getSelectedItem().toString();
            String dep = departure.getSelectedItem().toString();

            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline_resevation", "root", "dineshdk");

            pre = con.prepareStatement("Select * from flight where arrival =? and departure=?");
            pre.setString(1, arr);
            pre.setString(2, dep);

            ResultSet rs = pre.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();//to get the data in original format

            int cc = rsmd.getColumnCount();
            DefaultTableModel dt = (DefaultTableModel) table.getModel();
            
            while (rs.next()) {
                Vector v = new Vector();
                for (int i = 0; i < cc; i++) {
                    v.add(rs.getString("flight_id"));
                    v.add(rs.getString("flight_name"));
                    v.add(rs.getString("arrival"));
                    v.add(rs.getString("departure"));
                    v.add(rs.getString("duration"));
                    v.add(rs.getString("flight_date"));
                }
                dt.addRow(v);
            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(book_ticket.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

