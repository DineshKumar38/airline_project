    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                try {
            String f_id=flightid.getText();
            String f_name=flightname.getText();
            String arr=arrival.getText();
            String dep=departure.getText();
            String dur=duration.getText();
            String seat=seats.getText();
            String far=fare.getText();
            DateFormat db=new SimpleDateFormat("yyyy-mm-dd");
            String d=db.format(date.getDate());
            
            Connection con;
            PreparedStatement pre;
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline_resevation", "root", "dineshdk");
            Class.forName("com.mysql.jdbc.Driver");
            pre=con.prepareStatement("insert into flight(flight_id,flight_name,arrival,departure,duration,seats,fare,flight_date) values (?,?,?,?,?,?,?,?)");
            
            pre.setString(1,f_id);
            pre.setString(2,f_name);
            pre.setString(3,arr);
            pre.setString(4,dep);
            pre.setString(5,dur);
            pre.setString(6,seat);
            pre.setString(7,far);
            pre.setString(8,d);
            
            pre.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"flight added successfully");
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(add_customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

