    public void autoId() throws SQLException {
        try {
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline_resevation", "root", "dineshdk");
            pre = con.prepareStatement("Select MAX(flight_id)from flight");
            ResultSet rs = pre.executeQuery();
            rs.next();

            if (rs.getString("MAX(flight_id)") == null) {
                flightid.setText("FL001");
            } else {
                long id = Long.parseLong(rs.getString("MAX(flight_id)").substring(2, rs.getString("MAX(flight_id)").length()));
                id++;
                flightid.setText("FL" + String.format("%03d", id));
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(add_customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

